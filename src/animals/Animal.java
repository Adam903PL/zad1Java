package animals;

public abstract class Animal {
    private int age;
    private boolean gender;

    public Animal(int age, boolean gender) {
        this.age = age;
        this.gender = gender;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public boolean isGender() {
        return gender;
    }

    public void setGender(boolean gender) {
        this.gender = gender;
    }

    public abstract boolean grow();
}
